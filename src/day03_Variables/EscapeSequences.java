package day03_Variables;

public class EscapeSequences {
	/*
	 \n:stands for new line
	 \t:horizontal tab
	 \\ :prints a back slash
	 \':prints single quote
	 \":prints double quote
	 \:backslash
	 /:front slash6
	 
	 
	 */
 public static void main(String[] args) {
	System.out.print("Hello Cybertek\n Batch 12 students");
	System.out.print("Tomorrow is off\n ");
	System.out.print("true\r");
	System.out.print("False\n\n\n");
	
	
	System.out.println("\tClass starts tomorrow");
	
	System.out.println("\\muhtar");
	
	System.out.println("\'Java");
	System.out.println("Book is called \" Games of Throne\"");
}
 
 
}
